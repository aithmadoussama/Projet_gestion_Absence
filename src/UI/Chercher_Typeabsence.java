    private void chercherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chercherActionPerformed
        String lib = libelle.getText();
        List<TypeAbsence> type_trouve = new ArrayList<>();

        if (lib.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Le champ est vide",
                    "Important",JOptionPane.WARNING_MESSAGE);
        } else {
            type_trouve = principal.typeabsenceservise.getTypeBylibelle(lib);

            if (type_trouve.size() == 0) {
                DefaultTableModel model = (DefaultTableModel) table_employe_trouves.getModel();
                model.setRowCount(0);
                JOptionPane.showMessageDialog(this, lib + " non trouvable");
                libelle.setText("");
                model.setNumRows(0);
            } else {
                JOptionPane.showMessageDialog(this, lib + " trouvé avec succès");
                DefaultTableModel model = (DefaultTableModel) table_employe_trouves.getModel();
                model.setRowCount(0);

                for (TypeAbsence t : type_trouve) {
                    model.addRow(new Object[]{
                        t.getId(),
                        t.getLibelle(),
                        t.getJustification()
                    });
                }
            }
        }

    }//GEN-LAST:event_chercherActionPerformed

    private void supprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supprimerActionPerformed
        DefaultTableModel model = (DefaultTableModel) table_employe_trouves.getModel();
        if (model.getRowCount() != 0) {
            if (id_selected != -1) {
                int choix = JOptionPane.showConfirmDialog(
                        this,
                        "Voulez-vous vraiment supprimer ce type d'absence ?",
                        "Confirmation",
                        JOptionPane.OK_CANCEL_OPTION
                );
                if (choix == JOptionPane.OK_OPTION) {
                    this.principal.typeabsenceservise.deleteTypeabsence(
                            principal.typeabsenceservise.getTypeabsenceByid(id_selected));

                    this.principal.charger_TypesAbsence_Table();
                    JOptionPane.showMessageDialog(this, "Type d'absence supprimé avec succès");
                    libelle.setText("");
                    model.setRowCount(0);
                    id_selected = -1;
                }
            } else {
                JOptionPane.showMessageDialog(this, "Aucun type d'absence sélectionné",
                        "Important",JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Aucun type trouvé\n"
                    + "Veuillez en chercher un");
        }


    }//GEN-LAST:event_supprimerActionPerformed

    private void modifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifierActionPerformed
        DefaultTableModel model = (DefaultTableModel) table_employe_trouves.getModel();

        if (model.getRowCount() != 0) {
            if (id_selected != -1) {
                ModifierTypeRechPnl modifierType = new ModifierTypeRechPnl(this);
                modifierType.setLocationRelativeTo(null);
                modifierType.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Aucun type sélectionné pour la modification",
                        "Important",JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Aucun type trouvé.\nVeuillez en chercher un");
        }
    }//GEN-LAST:event_modifierActionPerformed

