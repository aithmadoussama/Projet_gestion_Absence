    private void table_employe_trouvesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_employe_trouvesMouseClicked
        int selectedRow = table_employe_trouves.getSelectedRow();
        if (selectedRow != -1) {
            DefaultTableModel model = (DefaultTableModel) table_employe_trouves.getModel();
            id_selected = Integer.parseInt(model.getValueAt(selectedRow, 0).toString());
        }
    }//GEN-LAST:event_table_employe_trouvesMouseClicked

